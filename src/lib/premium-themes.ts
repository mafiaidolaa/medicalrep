/**
 * Get theme by ID
 */
export function getThemeById(id: string): Theme | undefined {
  return PREMIUM_THEMES.find(theme => theme.id === id);
}

/**
 * Get all theme IDs
 */
export function getThemeIds(): string[] {
  return PREMIUM_THEMES.map(theme => theme.id);
}

